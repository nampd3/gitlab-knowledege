# ---------------------------------------------------------------------------
# Placement strategies
# ---------------------------------------------------------------------------
#
# A "placement" is a directory prefix that ``is_go_source_file``
# either accepts (non-vendor) or rejects (any path containing a
# ``vendor`` directory segment). The four non-vendor prefixes
# correspond to the directory layouts observed in the four sample
# repositories: a flat ``internal/`` tree, an explicit ``pkg/``
# layout, a ``cmd/<binary>/`` entry-point tree, and a ``service/``
# subpackage tree. The four vendor prefixes correspond to the four
# shapes the design's vendor predicate must reject: top-level
# ``vendor/``, deeply-nested ``vendor/github.com/...``,
# ``vendor/example.com/lib/v2/`` semver suffix, and the
# mid-path ``internal/vendor/`` case (every shape pinned by
# :mod:`tests.unit.test_go_filter`).

#: Non-vendor directory prefixes. ``is_go_source_file`` accepts paths
#: with any of these prefixes provided no enclosing segment equals
#: ``vendor``. The empty string represents a root-level placement,
#: which the design's CLI recognizer treats specially only when the
#: file is named ``main.go`` (which none of the snippet suffixes
#: above are).
_NON_VENDOR_PREFIXES: Final[tuple[str, ...]] = (
    "",
    "internal/",
    "pkg/handlers/",
    "cmd/svc/",
    "service/",
)


#: Vendor directory prefixes. Every prefix here contains a directory
#: segment equal to ``vendor``, so ``is_go_source_file`` rejects every
#: path that gets one of these prefixes. The set covers top-level
#: ``vendor/``, deeply-nested ``vendor/<host>/<org>/<lib>/``, a
#: semver-suffixed ``vendor/<host>/<org>/<lib>/v2/`` form, and the
#: mid-path ``internal/vendor/`` shape pinned by
#: :func:`tests.unit.test_go_filter.test_is_go_source_file_rejects_vendor_segment_in_middle_of_path`.
_VENDOR_PREFIXES: Final[tuple[str, ...]] = (
    "vendor/",
    "vendor/github.com/pkg/errors/",
    "vendor/example.com/lib/v2/",
    "internal/vendor/foo/",
)


# ---------------------------------------------------------------------------
# Vendor-segment predicate (mirror of the production filter)
# ---------------------------------------------------------------------------


def _path_has_vendor_segment(path: str) -> bool:
    """Return ``True`` when any directory segment of ``path`` equals
    ``vendor``.

    Mirrors the rejection rule applied by
    :func:`project_knowledge_mcp.project_analyzer.go.go_filter.is_go_source_file`
    so the property's assertion is independent of that helper's
    implementation. Path separators are normalized to ``/`` before
    splitting; the file's basename is excluded from the directory
    inspection so a file literally named ``vendor.go`` is not
    misclassified.
    """

    normalized = path.replace("\\", "/")
    parts = normalized.split("/")
    return "vendor" in parts[:-1]


# ---------------------------------------------------------------------------
# Hypothesis strategies
# ---------------------------------------------------------------------------


@st.composite
def _mixed_placement_repo(
    draw: st.DrawFn,
) -> tuple[dict[str, str], frozenset[str]]:
    """Generate a Go repo whose files mix vendored and non-vendored paths.

    Returns a ``(files, vendor_paths)`` pair where:

    * ``files`` is the :class:`RepositoryContents.files` mapping.
    * ``vendor_paths`` is the set of file paths in ``files`` whose
      enclosing directories include a ``vendor`` segment. Used by the
      assertion path to substring-match every emitted description and
      every emitted source-location path against the concrete vendor
      file set this example actually contains.

    Each generated file's basename is prefixed with a unique sequence
    index ``f{idx}_`` so collisions across draws are impossible —
    every snippet can be drawn any number of times and the resulting
    paths remain distinct.
    """

    all_prefixes = _NON_VENDOR_PREFIXES + _VENDOR_PREFIXES
    placements = draw(
        st.lists(
            st.tuples(
                st.integers(min_value=0, max_value=len(_DETECTION_SNIPPETS) - 1),
                st.sampled_from(all_prefixes),
            ),
            min_size=0,
            max_size=8,
        ),
    )

    files: dict[str, str] = {}
    vendor_paths: set[str] = set()
    for idx, (snippet_idx, prefix) in enumerate(placements):
        suffix, source = _DETECTION_SNIPPETS[snippet_idx]
        path = f"{prefix}f{idx}_{suffix}"
        files[path] = source
        if _path_has_vendor_segment(path):
            vendor_paths.add(path)
    return files, frozenset(vendor_paths)


@st.composite
def _non_vendor_only_repo(draw: st.DrawFn) -> dict[str, str]:
    """Generate a Go repo whose files all sit at non-vendor paths.

    Used as the baseline for the "adding vendor copies is inert"
    refinement: the test duplicates every Go source under a
    ``vendor/<subtree>/`` prefix and asserts the produced
    :class:`ProjectProfile` is unchanged across the two analyses.
    """

    placements = draw(
        st.lists(
            st.tuples(
                st.integers(min_value=0, max_value=len(_DETECTION_SNIPPETS) - 1),
                st.sampled_from(_NON_VENDOR_PREFIXES),
            ),
            min_size=0,
            max_size=6,
        ),
    )

    files: dict[str, str] = {}
    for idx, (snippet_idx, prefix) in enumerate(placements):
        suffix, source = _DETECTION_SNIPPETS[snippet_idx]
        path = f"{prefix}f{idx}_{suffix}"
        files[path] = source
    return files


# ---------------------------------------------------------------------------
# Shared analyze() invocation
# ---------------------------------------------------------------------------


def _run_analyze(files: dict[str, str]) -> object:
    """Run :func:`analyze` against ``files`` with fixed identity fields.

    Keeping the identity fields (``project_id``, ``full_path``,
    ``analysis_branch``, ``commit_sha``, ``repo_description``) fixed
    across calls means that any difference observed between two
    analyses on different file sets is attributable to the file
    contents alone. The commit SHA is the conventional all-zero
    placeholder used by other property tests in this suite.
    """

    rc = RepositoryContents(
        gitlab_project_id=1,
        commit_sha="0" * 40,
        files=files,
    )
    return analyze(
        project_id=1,
        full_path="group/vendor-exclusion-test",
        analysis_branch="main",
        commit_sha="0" * 40,
        repo_description=None,
        repository_contents=rc,
    )


# ---------------------------------------------------------------------------
# Property 1A: No detection references a vendored path
# ---------------------------------------------------------------------------


@given(spec=_mixed_placement_repo())
@settings(
    max_examples=50,
    suppress_health_check=[HealthCheck.too_slow, HealthCheck.data_too_large],
)
def test_no_detection_references_a_vendored_path(
    spec: tuple[dict[str, str], frozenset[str]],
) -> None:
    """For every mixed-placement repo, no detection cites a vendored file.

    Validates Requirement 1.3: a Go source file under any directory
    segment named ``vendor`` SHALL NOT contribute to any detection in
    the produced :class:`ProjectProfile`. The property is checked
    against the full :func:`analyze` pipeline so a regression in any
    of the four sub-analyzers' dispatch surfaces immediately.
    """

    files, vendor_paths = spec
    profile = _run_analyze(files)

    # ------------------------------------------------------------------
    # External service dependencies: every source location must be
    # vendor-free, and none may match a file the test itself placed
    # under a vendor directory. The first check is the strong
    # invariant (a regression that leaks any vendor path, not just one
    # we generated, surfaces here); the second check is the targeted
    # check (a regression that happens to match a vendor path we
    # generated surfaces with the offending path named).
    # ------------------------------------------------------------------
    for service in profile.external_service_dependencies:
        for loc in service.source_locations:
            assert not _path_has_vendor_segment(loc.path), (
                f"external service {service.name!r} has source_location "
                f"path {loc.path!r} containing a 'vendor' directory "
                f"segment (Requirement 1.3)"
            )
            assert loc.path not in vendor_paths, (
                f"external service {service.name!r} cites generated "
                f"vendor file {loc.path!r}; vendor files must not "
                f"contribute detections (Requirement 1.3)"
            )

    # ------------------------------------------------------------------
    # Database table dependencies: same contract.
    # ------------------------------------------------------------------
    for table in profile.database_table_dependencies:
        for loc in table.source_locations:
            assert not _path_has_vendor_segment(loc.path), (
                f"database table {table.table_name!r} has "
                f"source_location path {loc.path!r} containing a "
                f"'vendor' directory segment (Requirement 1.3)"
            )
            assert loc.path not in vendor_paths, (
                f"database table {table.table_name!r} cites generated "
                f"vendor file {loc.path!r}; vendor files must not "
                f"contribute detections (Requirement 1.3)"
            )

    # ------------------------------------------------------------------
    # Abstract inputs and outputs: the four Go I/O recognizers encode
    # the source-file path into the description as an
    # ``at <path>:<line>`` suffix (or, for HTTP placeholder
    # descriptions, ``<dynamic at <path>:<line> on <recv>>``). A
    # vendored path that slipped through would surface as a substring
    # match against the concrete vendor files we generated. The
    # substring check is targeted (it can produce false negatives if
    # the recognizer used a different path encoding, but the
    # ``loc.path`` checks above pin the source-location-bearing
    # detection types). Together the two checks rule out every
    # observable leak.
    # ------------------------------------------------------------------
    for entry in profile.abstract_inputs:
        for vp in vendor_paths:
            assert vp not in entry.description, (
                f"abstract_input description {entry.description!r} "
                f"references generated vendor file {vp!r}; vendor "
                f"files must not contribute detections "
                f"(Requirement 1.3)"
            )
    for entry in profile.abstract_outputs:
        for vp in vendor_paths:
            assert vp not in entry.description, (
                f"abstract_output description {entry.description!r} "
                f"references generated vendor file {vp!r}; vendor "
                f"files must not contribute detections "
                f"(Requirement 1.3)"
            )


# ---------------------------------------------------------------------------
# Property 1B: Adding vendor copies is inert
# ---------------------------------------------------------------------------


@given(base_files=_non_vendor_only_repo())
@settings(
    max_examples=30,
    suppress_health_check=[HealthCheck.too_slow, HealthCheck.data_too_large],
)
def test_adding_vendor_copies_does_not_change_detections(
    base_files: dict[str, str],
) -> None:
    """Refinement: adding vendor copies of every Go source is inert.

    Given a baseline repository whose Go sources all sit at non-vendor
    paths, building an extended repository that adds a duplicate of
    every Go file under a ``vendor/dup/`` prefix MUST leave every
    detection list and the ``degraded_sections`` list of the produced
    :class:`ProjectProfile` unchanged. This is task 11.7's optional
    formulation, "copying a non-vendor file under ``vendor/``
    produces no additional detection".

    Because vendored files are rejected before reaching the tokenizer
    (:func:`is_go_source_file` is the single filter the parser
    consults), the extended repository's parser sees a file mapping
    identical to the baseline's. The four sub-analyzers therefore
    receive identical inputs and the four detection lists are
    identical by construction; ``degraded_sections`` is identical for
    the same reason — vendored files cannot trigger a
    ``SkipFileEvent`` from the parser layer.

    Only the four detection lists and the ``degraded_sections`` list
    are compared. ``produced_at`` differs across calls (analyze stamps
    each invocation with :func:`datetime.now`) and ``purpose_summary``
    is unaffected by vendor copies in principle but is excluded from
    the comparison because the purpose summarizer's candidate
    interleaving is exercised by Property 4
    (:mod:`tests.property.test_property_04_go_purpose_priority`).
    """

    base_profile = _run_analyze(base_files)

    extended_files = dict(base_files)
    for path, content in base_files.items():
        # Only Go files are subject to the vendor predicate; non-Go
        # files (which the strategy does not generate today, but
        # which a future generator extension might) would not change
        # detections regardless of placement.
        if path.endswith(".go"):
            extended_files[f"vendor/dup/{path}"] = content
    extended_profile = _run_analyze(extended_files)

    # Detection sets compare equal under Pydantic's value-equality
    # rule (frozen ``_FrozenModel`` subclasses define ``__eq__`` over
    # all fields), so list equality is exact set+order equality. The
    # aggregator produces detections in a deterministic order that is
    # a function of the file mapping alone (Requirement 11.4), so
    # adding vendor files cannot perturb the order either.
    assert base_profile.abstract_inputs == extended_profile.abstract_inputs, (
        "abstract_inputs differs after adding vendor copies; vendor "
        "files must not contribute detections (Requirement 1.3)"
    )
    assert base_profile.abstract_outputs == extended_profile.abstract_outputs, (
        "abstract_outputs differs after adding vendor copies; vendor "
        "files must not contribute detections (Requirement 1.3)"
    )
    assert (
        base_profile.external_service_dependencies
        == extended_profile.external_service_dependencies
    ), (
        "external_service_dependencies differs after adding vendor "
        "copies; vendor files must not contribute detections "
        "(Requirement 1.3)"
    )
    assert (
        base_profile.database_table_dependencies
        == extended_profile.database_table_dependencies
    ), (
        "database_table_dependencies differs after adding vendor "
        "copies; vendor files must not contribute detections "
        "(Requirement 1.3)"
    )

    # ``degraded_sections`` covers per-file skip strings (build
    # constraint, cgo, tokenization failure) appended by the four
    # ``_safe_*`` helpers. Vendored files are rejected before
    # reaching the parser, so no additional skip strings can appear
    # in the extended analysis; this assertion pins that contract.
    assert base_profile.degraded_sections == extended_profile.degraded_sections, (
        "degraded_sections differs after adding vendor copies; "
        "vendored files must not reach the parser (Requirement 1.3)"
    )
