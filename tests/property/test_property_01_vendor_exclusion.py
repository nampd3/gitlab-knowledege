# ruff: noqa: E501
# Feature: go-analyzer-support, Property 1: Vendor directories never contribute detections.
"""Property test for the vendor-directory exclusion (Requirement 1.3).

**Validates Requirements 1.3** (Property 1 in the design, task 11.7 in
``tasks.md``).

The vendor exclusion is implemented at the aggregator boundary in
:func:`project_knowledge_mcp.project_analyzer.go.go_filter.is_go_source_file`
and is the single place the four Go sub-analyzers depend on for
filtering vendored copies of third-party libraries. Per design §3
"Vendor-Directory Exclusion", any repository path containing a
directory segment equal to ``vendor`` (case-sensitive, after
normalizing ``\\`` to ``/``) is rejected before reaching the
tokenizer, so a vendored copy of a Go source file cannot contribute
to any of the four detection sections of a
:class:`~project_knowledge_mcp.models.ProjectProfile`.

This property exercises the full :func:`analyze` pipeline end-to-end
across the four Go sub-analyzers (purpose summarizer, I/O extractor,
external service detector, database table detector) on synthetic
repositories that mix:

* Non-vendor Go source files that exercise every recognizer with
  patterns drawn verbatim from the four sample repositories
  (``http.HandleFunc``, ``mux.Handle``, ``cron.New``/``AddFunc``,
  ``activemq``'s ``Subscribe`` and ``SendMessage`` shapes with
  ``domain.SubscriberConfig`` and ``domain.Message`` composite
  literals, ``os.Open``/``os.WriteFile``, ``fec_pool_service/pb``'s
  ``ExecuteQuery`` and ``esb-go-libs/dbadapter``'s
  ``PoolExecuteQuery``, the ``activemq.NewClient`` broker setup).
* Identical sources placed under a ``vendor/`` directory using the
  four shapes recognized as vendored by the design: top-level
  ``vendor/``, deeply-nested ``vendor/github.com/...``,
  ``vendor/example.com/.../v2/`` semver-suffixed paths, and
  mid-path ``internal/vendor/`` (every shape the unit test
  :mod:`tests.unit.test_go_filter` covers).

Two related properties are checked:

1. **No detection references a vendored path.** For every entry in
   :attr:`ProjectProfile.external_service_dependencies` and every
   entry in :attr:`ProjectProfile.database_table_dependencies`, every
   :class:`SourceLocation`'s ``path`` MUST NOT contain a ``vendor``
   directory segment. For every :class:`AbstractInput` and every
   :class:`AbstractOutput`, the ``description`` MUST NOT contain the
   string form of any vendored path generated by the test (the four
   Go I/O recognizers encode the source-file path into the
   description as an ``at <path>:<line>`` suffix, so a leaked
   vendored path would surface as a substring match).

2. **Adding vendor copies is inert.** Given any non-vendor Go
   repository, adding a duplicate of every Go source file under a
   ``vendor/<subtree>/`` prefix MUST NOT change any of the four
   detection lists or the ``degraded_sections`` list of the produced
   :class:`ProjectProfile`. This is the "copying a non-vendor file
   under ``vendor/`` produces no additional detection" formulation
   from task 11.7's optional refinement.

Both properties are end-to-end against :func:`analyze` so the test
exercises the same call path production traffic takes. The
sub-analyzer-internal predicate :func:`is_go_source_file` is unit
tested separately (Task 2.2, :mod:`tests.unit.test_go_filter`); this
property pins the aggregator's *use* of that predicate as the only
filter the four sub-analyzers depend on for vendor exclusion.
"""

